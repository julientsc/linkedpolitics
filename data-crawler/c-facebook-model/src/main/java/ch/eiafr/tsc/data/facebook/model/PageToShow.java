package ch.eiafr.tsc.data.facebook.model;

public class PageToShow {
	String id;
	String about;
	String category;
	int likes;
	String link;
	String name;
	String username;
	String website;
	int talkingAboutCount;
	int wereHereCount;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAbout() {
		return about;
	}
	public void setAbout(String about) {
		this.about = about;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getLikes() {
		return likes;
	}
	public void setLikes(int likes) {
		this.likes = likes;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public int getTalkingAboutCount() {
		return talkingAboutCount;
	}
	public void setTalkingAboutCount(int talkingAboutCount) {
		this.talkingAboutCount = talkingAboutCount;
	}
	public int getWereHereCount() {
		return wereHereCount;
	}
	public void setWereHereCount(int wereHereCount) {
		this.wereHereCount = wereHereCount;
	}
	
	
}
